import UIKit

extension UIColor {
    static var workColor:UIColor {
        return UIColor(named: "work")!
    }
    
    static var studyColor:UIColor {
        return UIColor(named: "study")!
    }
    
    static var exercise:UIColor {
        return UIColor(named: "excercise")!
    }
}
